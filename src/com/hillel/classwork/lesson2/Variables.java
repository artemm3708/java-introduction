package com.hillel.classwork.lesson2;

public class Variables {

    public static void main(String[] args) {

        byte myByte = 101;
        System.out.println(myByte);

        short myShort = 30000;
        System.out.println("this is short: " + myShort);

        int result = myByte + myShort;
        System.out.println(result);

        int myInt = 478_272_983;
        System.out.println(myInt);

        long myLong = 129_372_344_374_292L;
        System.out.println(myLong);

        float myFloat = 659.346F;
        System.out.println(myFloat);

        double myDouble = 687290.92749240;
        System.out.println(myDouble);

        boolean isValid = true;
        System.out.println(isValid);

        char myChar = '~';
        System.out.println(myChar);

        char ch2 = 800;
        System.out.println(ch2);

        char ch3 = '\u270C';
        System.out.println(ch3);

        String string = "test";
        System.out.println(string);


    }

}
